# Numbers
# Numbers are represented many ways in Ruby

# The most basic form of a number is called an integer. 
# It is represented by the whole number only, with no decimal point. 

# A more complex form of a number is called a float. 
# A float is a number that contains a decimal.

# Example of integer literals
puts 1, 2, 3, 50, 10, 4345098098

# Example of float literals
puts 1.2345, 2345.4267, 98.2234