# Expression and Return
# Every time you enter something into irb you see the => back, which is called a hash rocket (cool name, huh?), followed by whatever your Ruby expression returns. 
# When you type something in at the irb prompt you are creating an expression. 
# An expression is anything that can be evaluated, and pretty much everything you write in Ruby is an expression. 
# An expression in Ruby always returns something, even if that's an error message or nil. 

