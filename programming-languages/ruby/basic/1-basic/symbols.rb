# Symbols 
# Ruby symbols are created by placing a colon (:) before a word.

# Examples of symbols
puts :name
puts :a_symbol
puts :"surprisingly, this is also a symbol"

# Symbols don't have to be valid variable names if the symbol name is enclosed in quotes, 
# as the last example above demonstrates.

# Basically, a symbol is used when you want to reference something like a string but don't ever intend to print it to the screen or change it. 
# It is often referred to as an immutable (i.e. unchangeable) string. 

