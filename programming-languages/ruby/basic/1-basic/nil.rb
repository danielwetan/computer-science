# nil
# In programming, we need a way to express "nothing", 
# and in Ruby, we do this through something called nil. 
# A variable with a value of nil could be described as having 'nothing' or being 'completely empty', 
# or even just simply 'not any specific type'. A situation where this may occur is where output is expected but none is returned, such as:

x = nil 
puts x 

# check if something is a nil type 
puts x.nil?


