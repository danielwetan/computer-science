# division vs modulo
# division
puts 16 / 4

#modulo
# There is also an operator called the modulo operator. 
# This is represented by the % symbol.
# It can be referred to as the remainder operator as well, though that is not strictly correct. 
# In modulo expressions, the value to the left of the % is called the dividend, and the value to the right is called the modulus.
puts 16 % 4 
puts 16 % 5 
puts 16.remainder(5)
puts "--"
puts 16.divmod(5)

# multiplying and dividing floats
puts 15.0 / 4 
